package com.cvprado.clase_2;

public class Hola_Mundo {
    public static void main(String[] args) {
        System.out.println("Hola Mundo!"); // Imprime un string
        System.out.println(10); // Imprime un entero
        System.out.println(20.56); // Imprime un decimal con punto y no coma
        System.out.println(true); // Imprime un booleano
        System.out.println("j"); // Impriem un caracter (char)
    }
}
